	private int redPart;
	private int greenPart;
	private int bluePart;

	public Color(int redPart, int greenPart, int bluePart) {
		this.redPart = redPart;
		this.greenPart = greenPart;
		this.bluePart = bluePart;
	}

	public Color(String s) {
		String s1 = s.toLowerCase();
		if (s1.startsWith("#")) {
			redPart = hexToInt(s1.substring(1, 3));
			greenPart = hexToInt(s1.substring(3, 5));
			bluePart = hexToInt(s1.substring(5, 7));
		}
	}

	public static Color parse(String s) {
		return new Color(s);
	}

	private int hexToInt(char c) {
		if (c >= '0' && c <= '9')
			return c - 48;
		if (c >= 'a' && c <= 'f')
			return 10 + (c - 97);
		if (c >= 'A' && c <= 'F')
			return 10 + (c - 65);
		else
			return 0;
	}

	private int hexToInt(String s) {
		return 16 * hexToInt(s.charAt(0)) + hexToInt(s.charAt(1));
	}

	public static Color mapString(String s) {
		return new Color(s);
	}

	private String decToHex(int i) {
		String s = Integer.toHexString(i);
		if (s.length() == 1)
			s = "0" + s;
		return s.toUpperCase();
	}

	public String toString() {
		return toHtml();
	}

	public String toHtml() {
		return "#" + toHex();
	}

	public String toHex() {
		return decToHex(redPart) + decToHex(greenPart) + decToHex(bluePart);
	}

	public String toDez() {
		StringBuffer stringbuffer = new StringBuffer();
		stringbuffer.append(redPart).append(",").append(greenPart).append(",").append(bluePart);
		return stringbuffer.toString();
	}

	public int toRGB() {
		return (redPart << 16) + (greenPart << 8) + bluePart;
	}

	public int blue() {
		return bluePart;
	}

	public int green() {
		return greenPart;
	}

	public int red() {
		return redPart;
	}

