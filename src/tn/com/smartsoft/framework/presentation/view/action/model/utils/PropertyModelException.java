package tn.com.smartsoft.framework.presentation.view.action.model.utils;

public class PropertyModelException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public PropertyModelException() {
		super();
	}

	public PropertyModelException(String message, Throwable cause) {
		super(message, cause);
	}

	public PropertyModelException(String message) {
		super(message);
	}

	public PropertyModelException(Throwable cause) {
		super(cause);
	}

}
